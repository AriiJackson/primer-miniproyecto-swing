    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        
        //Carga de datos
        names[indiceCount] = textoName.getText();
        lastnames[indiceCount] = textoLastname.getText();
        dni_s[indiceCount] = textoDNI.getText();
        address_s[indiceCount] = textoAddress.getText();
        phones[indiceCount] = textoPhone.getText();
        birthDates[indiceCount] = textoBirthDate.getText();
        
    }//GEN-LAST:event_aceptarActionPerformed

    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        //Incremento del indice en 1
        indiceCount++;
        
        //Control para no desbordar los arreglos
        if (indiceCount <= 9) {
            
            //Muestra los campos - para ingresar datos
            textoName.setText(names[indiceCount]);
            textoLastname.setText(lastnames[indiceCount]);
            textoDNI.setText(dni_s[indiceCount]);
            textoAddress.setText(address_s[indiceCount]);
            textoPhone.setText(phones[indiceCount]);
            textoBirthDate.setText(birthDates[indiceCount]);
            
            //Muestro el indice en el que estoy parado
            mostrarIndice.setText(Integer.toString(indiceCount+1));
            
        }
        else {
            //En caso de estar por desbordar, descremento en 1
            indiceCount--;
        }
        
    }//GEN-LAST:event_nextActionPerformed

    private void backActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backActionPerformed
        //Decremento del indice en 1
        indiceCount--;
        
        //Control para no desbordar los arreglos
        if (indiceCount >= 0) {
            
            //Mostrar los campos - ingresados 
            textoName.setText(names[indiceCount]);
            textoLastname.setText(lastnames[indiceCount]);
            textoDNI.setText(dni_s[indiceCount]);
            textoAddress.setText(address_s[indiceCount]);
            textoPhone.setText(phones[indiceCount]);
            textoBirthDate.setText(birthDates[indiceCount]);
            
            //Muestra el indice en el que estoy parado
            mostrarIndice.setText(Integer.toString(indiceCount+1));
            
        }
        else {
            //En caso de estar por desbordar, incrementa en 1
            indiceCount++;
        }
        
    }//GEN-LAST:event_backActionPerformed

